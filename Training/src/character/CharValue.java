package character;

public class CharValue {
	public static void main(String[] args) {        
		   // Create the first character object.  
		       Character char1 = new Character('A');  
		       System.out.print("The character value of char1 is = ");  
		       System.out.println(char1.charValue());   
		   // Create the second character object.  
		       Character char2 = new Character('B');  
		       System.out.print("The character value of char2 is = ");  
		       System.out.println(char2.charValue());  
	}  
	
}
